#include "Inf_BDR6120.h"

void Inf_BDR6120_Init(void)
{
    gpio_config_t io_config = {};
    io_config.intr_type = GPIO_INTR_DISABLE;
    io_config.mode = GPIO_MODE_OUTPUT;
    io_config.pull_down_en = GPIO_PULLDOWN_DISABLE;
    io_config.pull_up_en = GPIO_PULLUP_DISABLE;
    io_config.pin_bit_mask = (1 << INF_BDR_INA)|(1 << INF_BDR_INB);
    gpio_config(&io_config);

    gpio_set_level(INF_BDR_INA, 0);
    gpio_set_level(INF_BDR_INB, 0);

}

void Inf_BDR6120_Forward(void)
{
    gpio_set_level(INF_BDR_INA, 1);         
    gpio_set_level(INF_BDR_INB, 0);
}

void Inf_BDR6120_Back(void)
{
    gpio_set_level(INF_BDR_INA,0);
    gpio_set_level(INF_BDR_INB,1);
}

void Inf_BDR6120_Break(void)
{
    gpio_set_level(INF_BDR_INA,1);
    gpio_set_level(INF_BDR_INB,1);
}

void Inf_BDR6120_Open(void)
{
    Inf_BDR6120_Forward();
    vTaskDelay(1000);

    Inf_BDR6120_Break();
    vTaskDelay(1000);

    Inf_BDR6120_Back();
    vTaskDelay(1000);

    Inf_BDR6120_Break();
}
